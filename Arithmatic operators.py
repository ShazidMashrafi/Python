# + is addition
# - is subtraction
# * is multiplication
# / is division, answer will always be in float
# % is modulus, gives the remainder

x=11
y=4

print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(8/2)   # will always be in float
print(x%y)

# // is floor
print(x/y)    # will get a float value
print(x//y)   # will get the floor value of the result of division, will be an integer
print(7/2)
print(7//2)

# ** is Exponentiation
print(x**y)
print(2**5)